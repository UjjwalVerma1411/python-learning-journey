# A data type tells Python what kind of value an object contains and what operations can be performed on it.
# The object has the type, not the variable.



# Types of Data Types

# Numeric Data Types
# Integer
x = 5

# Float
y = 3.14

# Complex
z = 2 + 3j


# Boolean Data Type
a = True
b = False


# Text Data Type
#String
c = "Hello"


# Sequence Data Types
# List
cars = ["Bugatti", "Pagani", "Koenigsegg"]

# Tuple
odd_numbers_till_10 = (1, 3, 5, 7, 9)

# Range
numbers_till_10 = range(10)



# Mapping Data Type
# Dictionary
student = {
    "name": "Ujjwal",
    "age": 18,
    "college": "MUJ"
}



# Set Data Type
# Set
unique_numbers = {1, 2, 3, 4, 5}

# Frozenset
immutable_numbers = frozenset({1, 2, 3, 4, 5})



# Binary Data Types
# Bytes
byte_data = b"Hello"

# Bytearray
byte_array_data = bytearray(5)

# Memoryview
memory_view_data = memoryview(bytearray(5))



# None Data Type
# None
no_value = None